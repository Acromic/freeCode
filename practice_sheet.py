# Comparison Operators

x = 5

if x == 5:                                # Equals 5
    print('Equals 5')
if x > 4:                                 # Greater Than 4
    print('Greater than 4')
if x >= 5:                                # Greater than or Equals 5
    print('Greater than or Equals 5')
if x < 6:                                 # Less than 6
    print('Less than 6')
if x <=5:                                 # Less than or Equals 5
    print('Less than or Equals 5')
if x != 6:                                # Not equal 6
    print('Not equal 6')